#!/usr/bin/env python3
"""
Feature Verbalization Script
=============================
Reads features_final.csv and produces a verbalized description for each sample.

Each output row has two new columns:
  1. `feature_summary`  — Formatted feature numbers directly from the CSV (easy to parse).
  2. `quality_description` — Descriptive text generated by gemma4:e2b via Ollama (temperature=0).

Two prose modes:
  * legacy (default): free prose with the LLM phrasing each feature inline.
                      Used by the existing report's Phase-2 checkpoints.
  * --tagged:         prose where every numerical claim is pre-wrapped in
                      `<f_NAME>…</f>` spans (see src/feature_tags.py). The LLM
                      reproduces the spans verbatim and connects them with
                      natural English. The spans are pre-built from the CSV in
                      Python, so the LLM cannot hallucinate values.

Usage:
    python feature_verbalization.py                         # process all rows, legacy prose
    python feature_verbalization.py --tagged                # tagged prose (EMNLP rework)
    python feature_verbalization.py --limit 5               # first 5 rows only
    python feature_verbalization.py --input my.csv          # custom input
"""

import csv
import json
import argparse
import os
import sys
import urllib.request
import urllib.error
import math

# Import the tag SoT. The script lives in scripts/, src/ is a sibling.
sys.path.insert(0, os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "src"))
from feature_tags import FEATURE_TAGS, render_tagged_span, TAG_BY_NAME  # noqa: E402

# ─── Ollama config ────────────────────────────────────────────────────────────
OLLAMA_URL = "http://localhost:11434/api/generate"
MODEL      = "gemma4:e2b"


def fmt(val, suffix="", decimal=2):
    """Format a value for display; return 'N/A' for empty/NaN."""
    if val is None or val == "" or (isinstance(val, float) and math.isnan(val)):
        return "N/A"
    try:
        return f"{float(val):.{decimal}f}{suffix}"
    except (ValueError, TypeError):
        return str(val)


# ─── Column → display / unit helpers (column-agnostic) ──────────────────────
#
# Adding a new feature column to the extraction CSV requires NO changes here,
# provided the column name ends in one of the known unit suffixes below. The
# verbalizer will auto-render "<pretty name> of <value> <unit>" and include it
# in the prompt's Cover list.

_SKIP_COLS = {"filename", "filepath", "feature_summary", "quality_description"}

# Tokens that should be upper-cased when they appear as a word in a column name.
_ACRONYMS = {"snr", "hnr", "srmr", "rt60", "cpp", "ltas", "f0", "sd", "rap"}

# Ordered longest-first so e.g. "_syl_sec" wins over "_sec".
_UNIT_SUFFIXES = [
    ("_db_oct",      "dB/octave"),
    ("_db_per_oct",  "dB/octave"),
    ("_syl_sec",     "syl/sec"),
    ("_per_min",     "per min"),
    ("_per_sec",     "per sec"),
    ("_db",          "dB"),
    ("_hz",          "Hz"),
    ("_pct",         "%"),
    ("_sec",         "s"),
    ("_ms",          "ms"),
    ("_st",          "semitones"),
]


def _present(val) -> bool:
    """True if a CSV value is usable (not empty/None/NaN/'N/A')."""
    if val is None or val == "":
        return False
    s = str(val).strip()
    if s == "" or s.upper() == "N/A" or s.lower() == "nan":
        return False
    if isinstance(val, float) and math.isnan(val):
        return False
    return True


def _strip_unit_suffix(col: str) -> tuple[str, str]:
    """Return (base_name_without_suffix, display_unit). Empty unit means no match."""
    for suf, unit in _UNIT_SUFFIXES:
        if col.endswith(suf):
            return col[: -len(suf)], unit
    return col, ""


def _pretty_name(col: str) -> str:
    """Turn a column name into a display name with acronyms upper-cased."""
    if col.startswith("praat_"):
        col = col[6:]
    base, _ = _strip_unit_suffix(col)
    words = []
    for w in base.split("_"):
        words.append(w.upper() if w.lower() in _ACRONYMS else w)
    name = " ".join(words)
    return name[:1].upper() + name[1:] if name else name


def _render_claim(col: str, val) -> str | None:
    """Render 'Pretty name of <val> <unit>' or None if skipped/missing."""
    if col in _SKIP_COLS or not _present(val):
        return None
    _, unit = _strip_unit_suffix(col)
    name = _pretty_name(col)
    return f"{name} of {val} {unit}".rstrip()


def build_feature_summary(row: dict) -> str:
    """
    Section 1: Structured feature numbers, directly from the CSV.
    Column-agnostic — emits every non-metadata column as '<col>=<value><unit>'.
    """
    parts = []
    for col, val in row.items():
        if col in _SKIP_COLS:
            continue
        if col == "overlap_segments":
            parts.append(f"overlap_segments={val or 'N/A'}")
            continue
        if not _present(val):
            parts.append(f"{col}=N/A")
            continue
        _, unit = _strip_unit_suffix(col)
        parts.append(f"{col}={val}{unit}")
    return " | ".join(parts)


def _parse_overlap_segments(raw: str, sample_rate: int = 16000) -> str:
    """Convert sample-index overlap segments to seconds.
    e.g. '40000-80000' -> '2.5-5.0s'
    e.g. '40000-80000;120000-160000' -> '2.5-5.0s, 7.5-10.0s'
    """
    if not raw or raw == "N/A" or raw.strip() == "":
        return "None"
    try:
        result = []
        for seg in raw.split(";"):
            start_s, end_s = seg.strip().split("-")
            start_sec = int(start_s) / sample_rate
            end_sec = int(end_s) / sample_rate
            result.append(f"{start_sec:.1f}-{end_sec:.1f}s")
        return ", ".join(result)
    except (ValueError, IndexError):
        return "None"


# ─── Tagged-prose path (EMNLP rework) ────────────────────────────────────────
#
# Per-feature value formatting. Mirrors src/feature_set.py for the 13 SFS-scored
# features and adds defaults for the extras (sample_rate, silence_ratio,
# overlap_segments). The CSV column for each tag is `tag.csv_col` from
# src/feature_tags.py; this dict is just the format string.
_TAG_FORMAT: dict[str, str] = {
    "snr": "{:.2f}", "hnr": "{:.2f}",
    "f0_mean": "{:.2f}", "f0_sd": "{:.2f}",
    "jitter": "{:.4f}", "shimmer": "{:.4f}",
    "srmr": "{:.4f}", "overlap_ratio": "{:.4f}",
    "silence_ratio": "{:.4f}",
    "speaking_rate": "{:.3f}", "articulation_rate": "{:.3f}",
    "pause_count": "{:d}", "pause_rate": "{:.3f}",
    "duration": "{:.3f}",
}


def _format_value_for_tag(tag, raw, sample_rate: int = 16000) -> str | None:
    """Format the CSV value for a given FeatureTag into the string that goes
    inside the tag's template. Returns None if the value is missing/unusable.

    Handles three special cases:
    - sample_rate: constant (16000), no CSV column.
    - overlap_segments: variable-length list. For the section-tagged path each
      range is wrapped in <r>…</r> markers so the inference-time hook can fire
      once per range and produce a per-range attention map. Strip-tags removes
      the markers at display time so the rendered prose still reads naturally:
          "overlap segments at 0.5-1.0s, 3.0-4.5s, and 7.0-9.0s"
    - pause_count: integer.
    """
    if tag.name == "sample_rate":
        return str(int(sample_rate))
    if tag.name == "overlap_segments":
        formatted = _parse_overlap_segments(raw or "", sample_rate=sample_rate)
        if formatted == "None":
            return None
        # Wrap each comma-separated range with <r>...</r> for per-range attention.
        ranges = [r.strip() for r in formatted.split(",") if r.strip()]
        return ", ".join(f"<r>{r}</r>" for r in ranges)
    if not _present(raw):
        return None
    fmt = _TAG_FORMAT.get(tag.name, "{}")
    try:
        if tag.name == "pause_count":
            return fmt.format(int(round(float(raw))))
        return fmt.format(float(raw))
    except (TypeError, ValueError):
        return None


def _build_tagged_spans(row: dict, sample_rate: int = 16000) -> list[str]:
    """For one CSV row, return the list of complete `<f_NAME>…</f>` strings.

    Order matches FEATURE_TAGS so the verbalizer's prompt is deterministic.
    Missing features are skipped — the verbalizer's prompt explicitly tells the
    LLM to use only the spans it's given and not invent new ones.
    """
    spans: list[str] = []
    for ft in FEATURE_TAGS:
        raw = None if ft.csv_col is None else row.get(ft.csv_col)
        value_str = _format_value_for_tag(ft, raw, sample_rate=sample_rate)
        if value_str is None:
            continue
        body = ft.template.format(value=value_str)
        spans.append(render_tagged_span(ft.name, body))
    return spans


def _build_section_bodies(row: dict, sample_rate: int = 16000) -> dict[str, str]:
    """Pre-build the inner-claims string for each section from CSV values.

    Returns a dict {section_name: "<f_X>...</f> and <f_Y>...</f>"} — i.e., the
    body that goes inside `<sec_X>…</sec>`. Missing features are silently
    skipped; if all of a section's features are missing, that section is also
    skipped (the LLM is told not to emit it).

    Uses src/section_tags.py::build_section_body which is the SoT for section
    membership and inner-claim phrasing templates.
    """
    from section_tags import SECTION_TAGS, build_section_body

    bodies: dict[str, str] = {}
    for sec in SECTION_TAGS:
        feature_values: dict[str, str] = {}
        for fname in sec.feature_names:
            ft = next(f for f in __import__("section_tags").FEATURE_TAGS if f.name == fname)
            raw = None if ft.csv_col is None else row.get(ft.csv_col)
            value = _format_value_for_tag(ft, raw, sample_rate=sample_rate)
            if value is not None:
                feature_values[fname] = value
        body = build_section_body(sec.name, feature_values)
        if body:
            bodies[sec.name] = body
    return bodies


def generate_quality_description_sectioned(row: dict) -> str:
    """Section-structured prose generation (EMNLP rework, Path 3).

    Python pre-builds the inner <f_*>…</f> spans for each section from the CSV
    so the LLM cannot hallucinate values. The LLM's only job is to wrap each
    section's body in `<sec_NAME>…</sec>` and weave them into a coherent
    paragraph with minimal connective tissue.
    """
    bodies = _build_section_bodies(row)
    if not bodies:
        return "[ERROR] No usable features in row to build section bodies."

    # Render each section's full open-body-close so the LLM can reproduce verbatim.
    from section_tags import render_section_span
    section_spans: list[str] = []
    for name, body in bodies.items():
        section_spans.append(render_section_span(name, body))
    bullets = "\n".join(f"- {s}" for s in section_spans)

    overlap_str = _parse_overlap_segments(row.get("overlap_segments", ""))
    duration_line = ""
    if _present(row.get("duration_sec")):
        try:
            duration_line = f"Start the paragraph with: 'The recording is {float(row['duration_sec']):.3f} s long.'"
        except (TypeError, ValueError):
            pass

    prompt = f"""You are writing a quality description of a speech recording. You are given a list of pre-formatted SECTIONS, each wrapped in <sec_NAME>…</sec> tags with inner numerical claims wrapped in <f_FEATURE>…</f> tags. Your job is to assemble them into a coherent paragraph.

HARD RULES (non-negotiable):
- Reproduce every <sec_NAME>…</sec> block EXACTLY as given, character-for-character. Do not edit anything inside section tags.
- Do not invent new sections, tags, or claims.
- Emit every section at least once, in the given order.
- Outside the section tags you MAY add short natural-English connectives (e.g. ", ", "; ", " In addition,", " The recording also shows"). Keep these brief.
- {duration_line}
- No bullet points, no colons-as-separators. Output a single paragraph.
- If overlap is mentioned, you MAY append one un-tagged trailing sentence noting that F0 and formant estimates are unreliable during overlap windows.

Sections to reproduce verbatim, in order:
{bullets}

Overlap context: {overlap_str}
"""

    payload = {
        "model": MODEL, "prompt": prompt, "stream": False,
        "options": {"temperature": 0},
    }
    req = urllib.request.Request(
        OLLAMA_URL,
        data=json.dumps(payload).encode("utf-8"),
        headers={"Content-Type": "application/json"},
    )
    try:
        with urllib.request.urlopen(req, timeout=180) as response:
            result = json.loads(response.read().decode("utf-8"))
            text = result.get("response", "").strip()
            return " ".join(text.split())
    except Exception as e:
        return f"[ERROR] Ollama ({MODEL}): {e}"


def generate_quality_description_tagged(row: dict) -> str:
    """Tagged-prose generation. The Python side pre-builds every `<f_NAME>…</f>`
    span from the CSV, and the LLM's job is to weave them into a paragraph
    *verbatim*. This eliminates the failure mode where the LLM hallucinates
    values or paraphrases units.
    """
    spans = _build_tagged_spans(row)
    if not spans:
        return "[ERROR] No usable features in row to build tagged spans."

    overlap_str = _parse_overlap_segments(row.get("overlap_segments", ""))
    overlap_phrase = (
        f"Overlap is present at {overlap_str}. F0 and formant estimates may be unreliable during those segments."
        if overlap_str != "None"
        else "No overlap detected in this recording."
    )

    bullet_block = "\n".join(f"- {s}" for s in spans)

    # Note about range markers (<r>...</r>) — these appear inside <f_overlap_segments>
    # spans, one wrapper per time range. They look unusual but the LLM is told to
    # reproduce them verbatim like all the other tags; they trigger per-range
    # attention maps at inference time and get stripped before display.
    prompt = f"""You are writing a quality description of a speech recording. You are given a list of pre-formatted claims, each wrapped in tags. Your job is to assemble them into a coherent paragraph.

HARD RULES (non-negotiable):
- Reproduce every tagged span EXACTLY as given, character-for-character. Do not edit, paraphrase, reorder words inside a span, change capitalisation, or add words inside a span.
- You MAY connect spans with short natural-English phrases like "and", "while", "in addition", "the recording also shows", "the". Keep the connectives outside the tags.
- Emit every tagged span at least once. Do not invent new tags or claims.
- After all tagged spans appear, you MAY add one final un-tagged sentence about overall quality and the overlap context. Do not put any numerical claim in this sentence.
- No bullet points, no colons-as-separators. Output a single paragraph.

Tagged claims to reproduce verbatim:
{bullet_block}

Overlap context (mention in the un-tagged final sentence, if you write one): {overlap_phrase}
"""

    payload = {
        "model": MODEL,
        "prompt": prompt,
        "stream": False,
        "options": {"temperature": 0},
    }
    req = urllib.request.Request(
        OLLAMA_URL,
        data=json.dumps(payload).encode("utf-8"),
        headers={"Content-Type": "application/json"},
    )
    try:
        with urllib.request.urlopen(req, timeout=180) as response:
            result = json.loads(response.read().decode("utf-8"))
            text = result.get("response", "").strip()
            return " ".join(text.split())
    except Exception as e:
        return f"[ERROR] Ollama ({MODEL}): {e}"


def generate_quality_description(row: dict) -> str:
    """
    Section 2: Call gemma4:e2b via Ollama to produce a quality description.

    Column-agnostic. The Cover list is auto-built from every populated non-
    metadata column in the row, so adding new feature columns to the CSV
    requires no change here (provided the new column uses a known unit suffix).

    Prompt enforces:
      - every populated feature MUST appear in the output,
      - numeric claims use "of / is / =" phrasing (not ':') so the existing
        src/sfs.py ClaimParser recognises them,
      - abbreviations are kept verbatim — gemma4:e2b otherwise invents wrong
        expansions like "Linear Time Amplitude Spectrum" for LTAS.
    """
    overlap_segments_str = _parse_overlap_segments(row.get("overlap_segments", ""))

    # Build the Cover list from whatever is in the row.
    cover_items = []
    for col, val in row.items():
        if col == "overlap_segments":
            continue  # rendered separately below
        rendered = _render_claim(col, val)
        if rendered:
            cover_items.append(rendered)

    overlap_phrase = (
        f"Overlap segments at {overlap_segments_str}"
        if overlap_segments_str != "None"
        else "No overlap detected"
    )

    prompt = f"""You are an expert speech audio quality evaluator. Write a description of this speech sample in prose sentences (no bullets, no colons as the value separator).

HARD RULES (non-negotiable):
- Every feature listed in "Features" below MUST appear in the description.
- Phrase each feature as "<name> of <value> <unit>" OR "<name> is <value> <unit>" OR "<name> = <value> <unit>". Never use ":" as the separator between name and value. Never replace a number with an adjective alone.
- Quote every value verbatim with its unit; do not round, do not paraphrase.
- Keep abbreviations (F0, SD, RAP, SNR, HNR, SRMR, RT60, CPP, LTAS) exactly as written; do not invent expansions.
- Also describe the speaker-overlap information below. If overlap is present, state that F0 and formant estimates are unreliable during those segments.
- End with one sentence summarising the overall quality.
- Length is unlimited; completeness is mandatory.

Features: {', '.join(cover_items)}.

Speaker overlap: {overlap_phrase}.
"""

    payload = {
        "model": MODEL,
        "prompt": prompt,
        "stream": False,
        "options": {
            "temperature": 0,
        },
    }

    req = urllib.request.Request(
        OLLAMA_URL,
        data=json.dumps(payload).encode("utf-8"),
        headers={"Content-Type": "application/json"},
    )

    try:
        with urllib.request.urlopen(req, timeout=180) as response:
            result = json.loads(response.read().decode("utf-8"))
            text = result.get("response", "").strip()
            # Collapse to single line for CSV safety
            return " ".join(text.split())
    except Exception as e:
        return f"[ERROR] Ollama ({MODEL}): {e}"


def main():
    parser = argparse.ArgumentParser(description="Feature verbalization with Ollama")
    parser.add_argument("--input",  default="features_final.csv", help="Input CSV")
    parser.add_argument("--output", default="verbalized_features_final_example.csv", help="Output CSV")
    parser.add_argument("--limit",  type=int, default=0, help="Limit rows (0 = all)")
    parser.add_argument("--offset", type=int, default=0, help="Skip first N rows (for parallel work)")
    parser.add_argument("--tagged", action="store_true",
                        help="Emit inline-tagged prose: every numerical claim wrapped in `<f_NAME>…</f>` "
                             "(intermediate EMNLP rework). Default is legacy untagged prose.")
    parser.add_argument("--section-tagged", action="store_true",
                        help="Emit section-structured tagged prose: each quality dimension wrapped in "
                             "`<sec_X>…</sec>` with inner `<f_*>…</f>` claims (EMNLP rework Path 3). "
                             "Overrides --tagged when both are set.")
    args = parser.parse_args()

    mode = ("section-tagged (EMNLP rework Path 3)" if args.section_tagged
            else "inline-tagged" if args.tagged
            else "legacy untagged")
    print(f"Feature Verbalization ({mode})")
    print(f"  Model:  {MODEL} (temperature=0)")
    print(f"  Input:  {args.input}")
    print(f"  Output: {args.output}")
    if args.offset:
        print(f"  Offset: {args.offset} rows")
    if args.limit:
        print(f"  Limit:  {args.limit} rows")
    print()

    with open(args.input, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        rows = list(reader)

    rows = rows[args.offset:]
    if args.limit:
        rows = rows[:args.limit]
    total = len(rows)
    results = []

    if args.section_tagged:
        generate_fn = generate_quality_description_sectioned
    elif args.tagged:
        generate_fn = generate_quality_description_tagged
    else:
        generate_fn = generate_quality_description

    for i, row in enumerate(rows):
        fname = row.get("filename", "?")
        print(f"[{i+1}/{total}] {fname} ... ", end="", flush=True)

        # Section 1: formatted feature numbers
        feature_summary = build_feature_summary(row)

        # Section 2: LLM quality description
        quality_desc = generate_fn(row)

        row["feature_summary"] = feature_summary
        row["quality_description"] = quality_desc
        results.append(row)

        # Show a snippet
        snippet = quality_desc[:100] + "..." if len(quality_desc) > 100 else quality_desc
        print(f"done → {snippet}")

    # Save
    if results:
        fieldnames = list(results[0].keys())
        with open(args.output, "w", newline="", encoding="utf-8") as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(results)
        print(f"\n✅ Done! Saved {len(results)} rows to {args.output}")


if __name__ == "__main__":
    main()
